/**
 * Shared Icon component for React
 * Uses dangerouslySetInnerHTML for SVG paths (safe since paths are static)
 */
export function Icon({
  name,
  size = 20,
  className = '',
  strokeWidth = 2
}: IconProps) {
  // Support both PascalCase and camelCase icon names
  const normalizedName = iconAliases[name] || name;
  const path = iconPaths[normalizedName];

  if (!path) {
    // Return empty placeholder for unknown icons
    return (
      <span
        className={className}
        style={{
          width: size,
          height: size,
          display: 'inline-block',
          background: 'var(--color-bg-tertiary, #2f343d)',
          borderRadius: 2
        }}
        aria-hidden="true"
      />
    );
  }

  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth={strokeWidth}
      strokeLinecap="round"
      strokeLinejoin="round"
      className={className}
      aria-hidden="true"
      dangerouslySetInnerHTML={{ __html: path }}
    />
  );
}

